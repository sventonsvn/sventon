<div class="header">Current sventon features</div>
<div class="body">
<ul>
  <li>Easy installation and setup.</li>
  <li>WebDAV (http/https), svnserve (svn/svn+ssh) and file repository browsning.</li>
  <li>Support for multiple repositories.</li>
  <li>View and download files for any given revision or date in time.</li>
  <li>Sort or filter directory listings.</li>
  <li>View contents of archives and files inside archives.</li>
  <li>View logs history and metadata for files and directories.</li>
  <li>Log cache powered by <a href="http://lucene.apache.org/">Lucene</a>.</li>
  <li>Source code syntax highlighting using <a href="https://jhighlight.dev.java.net">JHighlight</a>.</li>
  <li>Style sheet marked up for easy graphical customization.</li>
  <li>Single account authentication support for non public repositories.</li>
  <li>Caching for fast access.</li>
  <li>File and directory name search (CamelCase enabled).</li>
  <li>Directory flattening for fast navigation.</li>
  <li>Image thumbnail preview.</li>
  <li>Diff files between revisions or directories, side-by-side or as unified diff.</li>
  <li>Keyword expansion when viewing or diffing files.</li>
  <li>Revision details as context sensitive RSS feeds.</li>
  <li>Customizable RSS layout.</li>
  <li>Display of file lock information.</li>
  <li>Download files and directories as ZIP files.</li>
  <li>Bypass of empty middle directories for faster navigation.</li>
  <li>Support for multiple encodings.</li>
</ul>
</div>